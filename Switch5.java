package SwitchCasePack;

public class Switch5 {

	public static void main(String[] args) {
		String d = "May";
		switch(d)
		{
		case "Ap" : System.out.println("April Month");
		   		break;
		case "Ja" : System.out.println("January Month");
   				break;
		case "De" : System.out.println("December Month");
   				break;
		case "Ju" : System.out.println("June Month");
   				break;
		case "Jl" : System.out.println("July Month");
   				break;
		case "Fe" : System.out.println("Febuary Month");
   				break;
		case "Se" : System.out.println("September Month");
   				break;
		case "Ot" : System.out.println("October Month");
   				break;
		case "My" : System.out.println("May Month");
   				break;
		case "Ma" : System.out.println("March Month");
   				break;
		case "No" : System.out.println("November Month");
   				break;
		case "Au" : System.out.println("August Month");
   				break;
   		default : System.out.println("Entered Month Code is Incorrect");
   				break;
   		
		
   		}

	}

}
